class Solution:
    # @param A : list of list of integers
    # @return a list of integers
    def solve(self, A):
        n = len(A)
        hash_map = dict()
        ans = []
        for i in range(n):
            m = len(A[i])
            sum = 0
            for j in range(m - 1, -1, -1):
                sum += A[i][j] * (2 ** j)

            if sum not in hash_map:
                hash_map[sum] = [i + 1]
            else:
                hash_map[sum].append(i + 1)
        print(hash_map)
        ans = []
        for key in hash_map.keys():
            if len(hash_map[key]) >= 2:
                ans.extend(hash_map[key][1:])

        return sorted(ans)


